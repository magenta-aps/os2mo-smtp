    datetime_obj = datetime.datetime(
        2022, 8, 15, tzinfo=pytz.timezone("Europe/Copenhagen")
    )

    assert mo_datestring_to_utc(datetime_obj) == datetime.datetime(2022, 8, 15)
    assert mo_datestring_to_utc(None) is None
