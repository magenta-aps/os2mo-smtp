"""Integration tests for handle_related_units."""

from datetime import datetime
from unittest.mock import MagicMock
from uuid import uuid4

import pytest

from mo_smtp.agents import handle_related_units
from mo_smtp.autogenerated_graphql_client import GraphQLClient


@pytest.mark.integration_test
async def test_related_units_not_found(
    context,
    graphql_client: GraphQLClient,
    email_client: MagicMock,
):
    """When the related_unit UUID doesn't exist, no email is sent."""
    await handle_related_units(context, uuid4(), None, graphql_client)
    email_client.send_email.assert_not_called()
