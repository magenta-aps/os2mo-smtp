@pytest.mark.integration_test
@pytest.mark.envvar({"ALERT_MANAGER_REMOVAL_USE_ORG_UNIT_EMAILS": "false"})
async def test_ituser_content_hash_update_sends_second_email(
    context,
    session: AsyncSession,
    graphql_client: GraphQLClient,
    email_client: MagicMock,
    root_loen_org: UUID,
):
    """When the data changes (new rolebinding added), the content hash differs
    and a second email is sent even though the ituser UUID is the same."""
    ituser_uuid, _ = await _setup_ituser(graphql_client, root_loen_org)

    # First call: sends email and stores the content hash
    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)
    assert email_client.send_email.call_count == 1

    # Flush the session so the SentAlert row is visible to the next call
    await session.flush()

    # Add a second rolebinding with a different role, changing the content
    role_facet = (await graphql_client._testing__get_role_facet()).objects[0].uuid
    new_role = await graphql_client._testing__create_class(
        name="superadmin",
        facet_uuid=role_facet,
        from_=datetime(2010, 1, 1),
    )
    await graphql_client._testing__create_rolebinding(
        ituser=ituser_uuid,
        role=new_role.uuid,
        from_=datetime(2010, 1, 1),
    )

    # Second call: content changed, so a new email should be sent
    await alert_on_ituser(context, ituser_uuid, None, graphql_client, session)
    assert email_client.send_email.call_count == 2
